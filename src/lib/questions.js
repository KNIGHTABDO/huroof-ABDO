/**
 * Repetition prevention system using localStorage.
 * Tracks seen IDs per letter.
 */
function getSeenIds(letter) {
  if (typeof window === 'undefined') return [];
  try {
    const seen = localStorage.getItem(`seen_q_${letter}`);
    return seen ? JSON.parse(seen) : [];
  } catch (e) {
    return [];
  }
}

function saveSeenId(letter, id) {
  if (typeof window === 'undefined') return;
  try {
    const seen = getSeenIds(letter);
    if (!seen.includes(id)) {
      seen.push(id);
      localStorage.setItem(`seen_q_${letter}`, JSON.stringify(seen));
    }
  } catch (e) {}
}

function clearSeenIds(letter) {
  if (typeof window === 'undefined') return;
  localStorage.removeItem(`seen_q_${letter}`);
}

/**
 * Get questions for a specific letter.
 * Returns array of question objects.
 */
export function getQuestionsForLetter(letter) {
  return QUESTIONS_WITH_IDS.filter(q => q.letter === letter);
}

/**
 * Get one random question for a specific letter smartly.
 * It won't repeat a question until all questions for that letter are used.
 * Returns null if no question exists for that letter.
 */
export function getRandomQuestion(letter) {
  const allForLetter = getQuestionsForLetter(letter);
  if (allForLetter.length === 0) return null;

  const seenIds = getSeenIds(letter);
  let available = allForLetter.filter(q => !seenIds.includes(q.id));

  // If we have shown all questions for this letter, clear history and start over
  if (available.length === 0) {
    clearSeenIds(letter);
    available = allForLetter;
  }

  // Pick a random question from available ones
  const selected = available[Math.floor(Math.random() * available.length)];
  saveSeenId(letter, selected.id);

  return selected;
}

